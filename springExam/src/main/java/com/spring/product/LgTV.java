package com.spring.product;

public class LgTV {

	public void turnOn() {
		System.out.println("LgTV---전원 켠다.");
	}
	
	public void turnOff() {
		System.out.println("LgTV---전원 끈다.");
	}
	
	public void soundUp() {
		System.out.println("LgTV---소리를 올린다.");
	}
	
	public void soundDown() {
		System.out.println("LgTV---소리를 내린다.");
	}
	
}
